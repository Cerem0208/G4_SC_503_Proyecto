    private void EditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EditarActionPerformed
        int filaSeleccionada = TableBono.getSelectedRow();

        if (filaSeleccionada != -1) {
            int idBono = (int) TableBono.getValueAt(filaSeleccionada, 0);
            String nombre_Bono = (String) TableBono.getValueAt(filaSeleccionada, 1);
            String monto = String.valueOf( TableBono.getValueAt(filaSeleccionada, 2));
            String anios_necesarios = String.valueOf( TableBono.getValueAt(filaSeleccionada, 3));

            agregarBono.Crud_ = "Edición";
            agregarBono.textoBoton_ = "Actualizar";
            agregarBono.isEdit_ = true;
            agregarBono.ID_aEditar_ = idBono;
            agregarBono = new AgregarBono();
            agregarBono.setVistaBono(this);
            agregarBono.setDatosEdicion(nombre_Bono, monto, anios_necesarios);
            agregarBono.setVisible(true);
        } else {
            popups.error("Por favor, seleccione un registro para editar.");
        }
    }//GEN-LAST:event_EditarActionPerformed


