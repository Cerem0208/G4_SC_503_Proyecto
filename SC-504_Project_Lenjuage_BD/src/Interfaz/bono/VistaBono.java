     public void cargarDatos() {
        DefaultTableModel model = (DefaultTableModel) TableBono.getModel();
        model.setRowCount(0);
        ConectionDB conexionDB = new ConectionDB();
        try (Connection conn = conexionDB.getConnection(); CallableStatement stmt = conn.prepareCall("{call Bonos.PROCEDURE_VISTA_Bonos(?)}")) {         
            stmt.registerOutParameter(1, java.sql.Types.REF_CURSOR);

            stmt.execute();

            try (ResultSet rs = (ResultSet) stmt.getObject(1)) {
                while (rs.next()) {
                    Object[] fila = new Object[4];
                    fila[0] = rs.getInt("ID_Bono");
                    fila[1] = rs.getString("sNombre_beneficio");
                    fila[2] = rs.getInt("nBono");
                    fila[3] = rs.getInt("Anos_Minimos");
                    model.addRow(fila);
                }
                for (int i = 0; i < TableBono.getColumnCount(); i++) {
                    DefaultTableCellRenderer centerRenderer = new DefaultTableCellRenderer();
                    centerRenderer.setHorizontalAlignment(DefaultTableCellRenderer.CENTER);
                    TableBono.getColumnModel().getColumn(i).setCellRenderer(centerRenderer);
                }
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    private void Ajustar_Tamano_Columnas(javax.swing.JTable tabla) {
        for (int i = 0; i < tabla.getColumnCount(); i++) {
            TableColumn column = tabla.getColumnModel().getColumn(i);
            int Ancho_Max = 0;
            for (int row = 0; row < tabla.getRowCount(); row++) {
                TableCellRenderer celdaRenderer = tabla.getCellRenderer(row, i);
                Object value = tabla.getValueAt(row, i);
                Component celda = celdaRenderer.getTableCellRendererComponent(tabla, value, false, false, row, i);
                Ancho_Max = Math.max(Ancho_Max, celda.getPreferredSize().width);
            }
            column.setPreferredWidth(Ancho_Max);
        }
    }

