    private void TableBonoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TableBonoMouseClicked
        if (evt.getButton() == MouseEvent.BUTTON3) {
            int x = evt.getX();
            int y = evt.getY();

            int row = TableBono.rowAtPoint(evt.getPoint());
            if (row >= 0) {
                TableBono.setRowSelectionInterval(row, row);
            }
            jPopup.show(evt.getComponent(), x, y);
        }
    }//GEN-LAST:event_TableBonoMouseClicked

