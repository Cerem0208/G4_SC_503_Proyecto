    private void TableRevisionMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TableRevisionMouseClicked
        if (evt.getButton() == MouseEvent.BUTTON3) {
            int x = evt.getX();
            int y = evt.getY();

            int row = TableRevision.rowAtPoint(evt.getPoint());
            if (row >= 0) {
                TableRevision.setRowSelectionInterval(row, row);
            }
            jPopup.show(evt.getComponent(), x, y);
        }
    }//GEN-LAST:event_TableRevisionMouseClicked

