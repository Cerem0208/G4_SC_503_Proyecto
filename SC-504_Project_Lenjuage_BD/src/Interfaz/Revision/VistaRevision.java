    private void EditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EditarActionPerformed
        int filaSeleccionada = TableRevision.getSelectedRow();

        if (filaSeleccionada != -1) {
            Object placaObject = TableRevision.getValueAt(filaSeleccionada, 1);
            String placa = placaObject.toString();

            Date fecha_inicio = (Date) TableRevision.getValueAt(filaSeleccionada, 4);
            Date fecha_fin = (Date) TableRevision.getValueAt(filaSeleccionada, 5);
            String duracion = (String) TableRevision.getValueAt(filaSeleccionada, 6);
            String mecanico = (String) TableRevision.getValueAt(filaSeleccionada, 7);

            agregarRevision.Crud_ = "Edición";
            agregarRevision.textoBoton_ = "Completar";
            agregarRevision.isEdit_ = true;

            agregarRevision.ID_aEditar_ = placa;
            agregarRevision = new AgregarRevision();
            agregarRevision.setVistaMantenimiento(this);

            agregarRevision.setDatosEdicion(fecha_inicio, fecha_fin, duracion, mecanico, placa);
            agregarRevision.setVisible(true);
        } else {
            popups.error("Por favor, seleccione un registro para editar.");
        }
    }//GEN-LAST:event_EditarActionPerformed

