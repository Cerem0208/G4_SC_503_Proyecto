    public void cargarDatos() {
        DefaultTableModel model = (DefaultTableModel) TableRevision.getModel();
        model.setRowCount(0);
        ConectionDB conexionDB = new ConectionDB();

        try (Connection conn = conexionDB.getConnection(); CallableStatement stmt = conn.prepareCall("{call Procedure_lista_revision(?)}")) {

            stmt.registerOutParameter(1, oracle.jdbc.OracleTypes.CURSOR);

            stmt.execute();
            try (ResultSet rs = (ResultSet) stmt.getObject(1)) {
                while (rs.next()) {
                    Object[] fila = new Object[9];
                    fila[0] = rs.getInt("ID_Revision");
                    fila[1] = rs.getInt("Placa_Bus");
                    fila[2] = rs.getString("Modelo_Bus");
                    fila[3] = rs.getString("Marca_Bus");
                    fila[4] = rs.getDate("dFecha_Inicio");
                    fila[5] = rs.getDate("dFecha_Fin");
                    fila[6] = rs.getString("sDuracion");
                    fila[7] = rs.getString("Nombre_Mantenimiento");
                    fila[8] = rs.getString("Estado_Bus");
                    model.addRow(fila);
                }

                for (int i = 0; i < TableRevision.getColumnCount(); i++) {
                    DefaultTableCellRenderer centerRenderer = new DefaultTableCellRenderer();
                    centerRenderer.setHorizontalAlignment(DefaultTableCellRenderer.CENTER);
                    TableRevision.getColumnModel().getColumn(i).setCellRenderer(centerRenderer);
                }
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    private void Ajustar_Tamano_Columnas(javax.swing.JTable tabla) {
        for (int i = 0; i < tabla.getColumnCount(); i++) {
            TableColumn column = tabla.getColumnModel().getColumn(i);
            int Ancho_Max = 0;
            for (int row = 0; row < tabla.getRowCount(); row++) {
                TableCellRenderer celdaRenderer = tabla.getCellRenderer(row, i);
                Object value = tabla.getValueAt(row, i);
                Component celda = celdaRenderer.getTableCellRendererComponent(tabla, value, false, false, row, i);
                Ancho_Max = Math.max(Ancho_Max, celda.getPreferredSize().width);
            }
            column.setPreferredWidth(Ancho_Max);
        }
    }

